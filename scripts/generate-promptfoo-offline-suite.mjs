import fs from 'node:fs/promises';
import path from 'node:path';
import process from 'node:process';
import { parse, stringify } from 'yaml';

function parseArgs(argv) {
  const args = {};
  for (let index = 0; index < argv.length; index += 1) {
    const token = argv[index];
    if (!token.startsWith('--')) {
      throw new Error(`Unexpected argument: ${token}`);
    }
    const key = token.slice(2);
    const value = argv[index + 1];
    if (!value || value.startsWith('--')) {
      throw new Error(`Missing value for --${key}`);
    }
    args[key] = value;
    index += 1;
  }
  return args;
}

function assertString(value, message) {
  if (typeof value !== 'string' || value.length === 0) {
    throw new Error(message);
  }
}

async function main() {
  const { source, fixture, output } = parseArgs(process.argv.slice(2));
  assertString(source, 'Expected --source');
  assertString(fixture, 'Expected --fixture');
  assertString(output, 'Expected --output');

  const rootDir = process.cwd();
  const sourcePath = path.resolve(rootDir, source);
  const fixturePath = path.resolve(rootDir, fixture);
  const outputPath = path.resolve(rootDir, output);

  const [sourceText, fixtureText] = await Promise.all([
    fs.readFile(sourcePath, 'utf8'),
    fs.readFile(fixturePath, 'utf8'),
  ]);

  const sourceSuite = parse(sourceText);
  if (!Array.isArray(sourceSuite)) {
    throw new Error('Expected source suite YAML to contain a top-level array');
  }

  const outputsByCaseId = JSON.parse(fixtureText);
  if (!outputsByCaseId || Array.isArray(outputsByCaseId) || typeof outputsByCaseId !== 'object') {
    throw new Error('Expected fixture JSON to be an object keyed by case_id');
  }

  const usedCaseIds = new Set();
  const derivedSuite = sourceSuite.map((testCase, index) => {
    if (!testCase || typeof testCase !== 'object' || Array.isArray(testCase)) {
      throw new Error(`Expected test case at index ${index} to be an object`);
    }

    const caseId = testCase?.vars?.case_id;
    assertString(caseId, `Missing vars.case_id for test case at index ${index}`);

    if (usedCaseIds.has(caseId)) {
      throw new Error(`Duplicate vars.case_id detected: ${caseId}`);
    }
    usedCaseIds.add(caseId);

    const providerOutput = outputsByCaseId[caseId];
    assertString(providerOutput, `Missing fixture output for case_id "${caseId}"`);

    return {
      ...testCase,
      providerOutput,
    };
  });

  const unknownFixtureKeys = Object.keys(outputsByCaseId).filter((caseId) => !usedCaseIds.has(caseId));
  if (unknownFixtureKeys.length > 0) {
    throw new Error(`Fixture contains unknown case_id entries: ${unknownFixtureKeys.join(', ')}`);
  }

  const header = [
    '# Generated from the canonical uplift suite and case-keyed replay fixtures.',
    '# Do not edit this file directly.',
    `# Source suite: ${path.relative(rootDir, sourcePath).replaceAll(path.sep, '/')}`,
    `# Fixture map: ${path.relative(rootDir, fixturePath).replaceAll(path.sep, '/')}`,
    '',
  ].join('\n');

  await fs.mkdir(path.dirname(outputPath), { recursive: true });
  await fs.writeFile(outputPath, `${header}${stringify(derivedSuite)}`, 'utf8');

  console.log(
    JSON.stringify(
      {
        generatedCases: derivedSuite.length,
        output: path.relative(rootDir, outputPath).replaceAll(path.sep, '/'),
      },
      null,
      2,
    ),
  );
}

main().catch((error) => {
  console.error(error instanceof Error ? error.message : String(error));
  process.exitCode = 1;
});
